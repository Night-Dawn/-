package com.example.demo.entity;

public class User {
    private int User_id;
    private String User_name;
    private char User_sex;
    private String User_address;
    private String User_phone;
    private String User_username;
    private String User_password;
    private int User_borrow_max;
    private int User_borrow_number;

    public int getUser_borrow_max() {
        return User_borrow_max;
    }

    public void setUser_borrow_max(int user_borrow_max) {
        User_borrow_max = user_borrow_max;
    }

    public int getUser_borrow_number() {
        return User_borrow_number;
    }

    public void setUser_borrow_number(int user_borrow_number) {
        User_borrow_number = user_borrow_number;
    }

    public int getUser_id() {
        return User_id;
    }

    public void setUser_id(int user_id) {
        User_id = user_id;
    }

    public String getUser_name() {
        return User_name;
    }

    public void setUser_name(String user_name) {
        User_name = user_name;
    }

    public char getUser_sex() {
        return User_sex;
    }

    public void setUser_sex(char user_sex) {
        User_sex = user_sex;
    }

    public String getUser_address() {
        return User_address;
    }

    public void setUser_address(String user_address) {
        User_address = user_address;
    }

    public String getUser_phone() {
        return User_phone;
    }

    public void setUser_phone(String user_phone) {
        User_phone = user_phone;
    }

    public String getUser_username() {
        return User_username;
    }

    public void setUser_username(String user_username) {
        User_username = user_username;
    }

    public String getUser_password() {
        return User_password;
    }

    public void setUser_password(String user_password) {
        User_password = user_password;
    }
}
